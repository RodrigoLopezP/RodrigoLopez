
#include <iostream>
using namespace std;
void main() {
	int contatore = 0;
	cout<<"Da questo punto inizia il ciclo da 1 a 10"<<endl;
/*Inizio del ciclo */
	for(int i=0; i<10;i++){
		contatore++;
		cout << contatore << endl;
	}
/*Fine del ciclo */
	cout<<"Il ciclo è finito"<<endl;
	system("pause");
}